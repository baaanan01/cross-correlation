a_dict = {'Информатика': ['70(л)', '20(пр)', '20(лаб)'], 'Физика': ['10(л)', '-', '10(лаб)'], 'Физкультура': ['-', '-', '100(лаб)']}
to_int = lambda x: int('0'+''.join([e for e in x if e.isdigit()]))
for k,v in a_dict.items():
    print(f'{k}={sum([to_int(e) for e in v])}')
# ...
# Информатика=110
# Физика=20
# Физкультура=100